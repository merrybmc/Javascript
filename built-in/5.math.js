// Math
// static properties, method

console.log(Math.E); // 오일러의 상수, 자연로그의 밑 값
console.log(Math.PI); // 원주율 PI 값

// stdtic method
console.log(Math.abs(-10)); // 절대값
console.log(Math.ceil(1.4)); // 소수점 이하 올림
console.log(Math.floor(1.4)); // 소수점 이하 내림
console.log(Math.round(1.4)); // 반올림
console.log(Math.round(1.6)); // 반올림
console.log(Math.trunc(1.5432)); // 정수만 반환
console.log(Math.max(1, 2, 3, 4, 5)); // 최대값 출력
console.log(Math.min(1, 2, 3, 4, 5)); // 최소값 출력
console.log(3 ** 2); // 거듭제곱
console.log(Math.pow(3, 2)); // 거듭제곱
console.log(Math.sqrt(9)); // 제곱근
console.log(Math.random()); // 0부터 1사이의 랜덤한 값을 반환
console.log(Math.random() * 10 + 1); // 1부터 10사이의 랜덤한 값을 반환
console.log(Math.trunc(Math.random() * 10 + 1)); // 1부터 10사이의 랜덤한 정수값을 반환
